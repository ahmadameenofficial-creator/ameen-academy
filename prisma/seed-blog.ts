async function main() {
  // جلب الأدمن
  const admin = await prisma.user.findFirst({
    where: { role: "ADMIN" },
  });

  if (!admin) {
    console.error("مفيش أدمن! لازم يكون في حساب أدمن الأول.");
    process.exit(1);
  }

  console.log(`Found admin: ${admin.name} (${admin.email})`);

  for (const article of articles) {
    const wordCount = article.content.split(/\s+/).length;
    const readingTime = Math.max(1, Math.ceil(wordCount / 200));

    const existing = await prisma.blogPost.findUnique({
      where: { slug: article.slug },
    });

    if (existing) {
      console.log(`Skipping "${article.title}" — already exists`);
      continue;
    }

    await prisma.blogPost.create({
      data: {
        ...article,
        authorId: admin.id,
        readingTime,
        publishedAt: new Date(),
      },
    });

    console.log(`Created: "${article.title}" (${readingTime} min read, ${wordCount} words)`);
  }

  console.log("\nDone! All articles seeded.");
}

main()
  .catch(console.error)
  .finally(() => prisma.$disconnect());
